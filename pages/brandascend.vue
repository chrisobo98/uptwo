<script setup>
import { onMounted } from "vue";

onMounted(() => {
  window.addEventListener("scroll", () => {
    const timeline = document.querySelector(".timeline_component");
    const progressBar = document.querySelector(".timeline_progress-bar");

    const rect = timeline?.getBoundingClientRect();
    const windowHeight = window.innerHeight;

    if (rect && rect.top < windowHeight && rect.bottom > 0) {
      const totalHeight = rect.height;
      const scrollY = window.scrollY;
      const scrollTop = rect.top + scrollY;

      const scrollProgress = Math.min(
        Math.max((scrollY + windowHeight / 2 - scrollTop) / totalHeight, 0),
        1
      );

      if (progressBar) {
        progressBar.style.height = `${scrollProgress * 100}%`;
      }
    }
  });
});
</script>

